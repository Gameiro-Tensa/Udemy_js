"use strict"

// Addition
console.log("5 + 6 =", 5 + 6 )

// Subtraktion
console.log("5 - 6 =", 5 - 6)

// Multiplikation
console.log("5 * 6 =", 5 * 6)

// Division
console.log("5 / 6 =", 5 / 6)

// Modulo (Rest bei der ganzzahligen Division)
console.log("11 % 6 =", 11 % 6)

// Potenz (2 hoch 10) 
console.log("2 ** 10 =", 2 ** 10)

//  Runden
console.log("Runden, Math.round(5.5):", Math.round(5.5))    // roundet nach oben
console.log("Abrunden, Math.floor(5.5):", Math.floor(4.55548))  // Zahlen nach komma werden weg
console.log("Aufrunden, Math.ceil(5.5):", Math.ceil(5.5))

// Maximun
console.log("Math.max(1, 2, 3, 4):", Math.max(1, 2, 3, 4))  // kann man mehrere Zahlen reinschreiben

// Minimun
console.log("Math.min(1, 2, 3, 4):", Math.min(1, 2, 3, 4))